package seedu.address.logic.helpers;

/**
 * Comparison operators for simple numeric checks.
 * EQ (=), GE (>=), LE (<=), GT (>), LT (<).
 */
public enum Comparison {
    EQ, // equal to
    GE, // greater than or equal to
    LE, // less than or equal to
    GT, // greater than
    LT // less than
}
